const groupUsers=(type)=>{
  return data.map(d=>monthlyBalance(d.first_name))
  .reduce((acc,d)=>{
      if (type === "banco"){
        if(acc[d.banco]){
          acc[d.banco]["cantidadUsuarios"]++;
          acc[d.banco]["ahorroTotal"] += d.ahorro
          }
        else{
          acc[d.banco]={
            bank:d.banco,
            cantidadUsuarios:1,
            ahorroTotal:d.ahorro
            }
          }
      }

      else if(type === "pais"){
          if(acc[d.country]){
            acc[d.country]["cantidadUsuarios"]++;
            acc[d.country]["ahorroTotal"] += d.ahorro
            }
          else{
            acc[d.country]={
              country:d.country,
              cantidadUsuarios:1,
              ahorroTotal:d.ahorro
              }
            }
        }
      return acc
        
    },{})
}

